// Function to reveal the project items one by one
function revealProjects() {
    const projects = document.querySelectorAll('.pdetails');
    projects.forEach((project, index) => {
        if (isInViewport(project)) {
            setTimeout(() => {
                project.classList.add('show');
            }, index * 300); // Adding delay for each project (300ms delay)
        }
    });
}

// Attach the scroll event listener
window.addEventListener('scroll', revealProjects);

// Trigger the animation when the page loads
window.addEventListener('load', revealProjects);
